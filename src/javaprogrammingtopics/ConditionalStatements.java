package javaprogrammingtopics;

public class ConditionalStatements {

	public static void main(String[] args) {
		
		String a= "qwer";
		
		if (a=="sunday") {
			System.out.println("Sunday");			
		}
		else if (a=="monday") {
			System.out.println("Monday");
		}
		else if (a=="Tuesday") {
			System.out.println("Monday");
		}
		else if (a=="Wednesday") {
			System.out.println("Monday");
		}
		else if (a=="Thursay") {
			System.out.println("Monday");
		}
		else if (a=="Friday") {
			System.out.println("Monday");
		}
		else {
			System.out.println("Saturday/Invalid");
		}
		

	}

}
